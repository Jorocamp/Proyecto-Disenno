    public boolean validarProbabilidadDestino(){
        int cantPisos = (Integer) spn_nPisos.getValue();
        float probabilidad = 0;

        for(int i = 0; i<cantPisos; i++){
            probabilidad += (Float) p2Lista.get(i).getValue();
        }
        
        if(probabilidad == 1.0){
            return true;
        }
        return false;
    }
    
    
